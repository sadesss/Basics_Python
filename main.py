import numpy as np

A = np.array(
    [
        [400 + (159999 / 400) * 1j, -(1 / 400) * 1j, -400],
        [-(1 / 400) * 1j, 200 + (319999 / 400) * 1j, 800j],
        [-400, 800j, 500 + 800j],
    ]
)

B = np.array([[300 + 200j], [100 + 200j], [0]])

# Найдем обратную матрицу A^(-1)
A_inv = np.linalg.inv(A)

# Умножим A^(-1) на B, чтобы найти x
x = np.dot(A_inv, B)

print("Решение x:")
print(x)
